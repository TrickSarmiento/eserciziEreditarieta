package esercizio2;

public class CategoriaMerceologica{
	
	private String nome;
	private String codice;
	
	public CategoriaMerceologica(String nome, String codice) {
		super();
		this.nome = nome;
		this.codice = codice;
	
	}

	public String getNome() {
		return nome;
	}

	public String getCodice() {
		return codice;
	}
	
	
}
